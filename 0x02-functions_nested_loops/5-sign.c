#include <stdio.h>
#include "main.h"
/**
 * print_sign- This is the print_sign function
 *
 * Description: This function prints the sign of a number
 * @n: The number whose sign we are looking for
 * Return: it returns 1 for +, 0 for 0, -1 for -
 */
int print_sign(int n)
{
	if (n > 0)
	{
		_putchar('+');
		return (1);
	}
	else if (n < 0)
	{
		_putchar('-');
		return (-1);
	}
	else
	{
		_putchar('0');
		return (0);
	}
	return (0);
}
