#include "3-calc.h"
#include <stdio.h>
#include <stdlib.h>
/**
 * op_add- This is the op_add function
 * Description: This function multiplies 2 intergers
 * @a: the first operand
 * @b: The second operand
 * Return: Returns and integer
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub- This is the op_sub function
 * Description: This function subtracts 2 intergers
 * @a: the first operand
 * @b: The second operand
 * Return: Returns and integer
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul- This is the op_mul function
 * Description: This function multiplies 2 intergers
 * @a: the first operand
 * @b: The second operand
 * Return: Returns and integer
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div- This is the op_div function
 * Description: This function divides 2 intergers
 * @a: the first operand
 * @b: The second operand
 * Return: Returns and integer
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod- This is the op_mod function
 * Description: This function mods 2 intergers
 * @a: the first operand
 * @b: The second operand
 * Return: Returns and integer
 */
int op_mod(int a, int b)
{
	return (a % b);
}
